s = "GeeksforGeeks"

# Retrieves characters from index 1 to 3: 'eek'
print(s[1:4])  

# Retrieves characters from beginning to index 2: 'Gee'
print(s[:3])   

# Retrieves characters from index 3 to the end: 'ksforGeeks'
print(s[3:])   

# Reverse a string
print(s[::-1])

# s[::-1]

# Syntax: s[start:end:step] → here step = -1.

# -1 means move backwards, so the string is reversed.

# "GeeksforGeeks" → "skeeGrofskeeG"